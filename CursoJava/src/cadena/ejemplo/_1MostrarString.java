package cadena.ejemplo;

public class _1MostrarString {

	public static void main(String[] args) {
		
		// println: Imprime el texto en pantalla y da un salto de línea
		System.out.println("Esta es una cadena y es un conjunto de caracteres");
		
		// print: Imprime texto en pantalla (sin salto de línea)
		System.out.print("El ");
		System.out.print("Solsticio ");
		System.out.print("de ");
		System.out.print("invierno ");
		System.out.print("es ");
		System.out.print("en ");
		System.out.print("Diciembre en el Hemisferio Norte.");
	}
}
