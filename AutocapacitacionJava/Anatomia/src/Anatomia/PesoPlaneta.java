package Anatomia;

public class PesoPlaneta {

	public static void main(String[] args) {
		System.out.print("Tu peso en la Tierra es de:"); //sin salto de línea
		double peso = 72;
		System.out.println(peso + " kilos"); //con salto de línea
		
		System.out.print("Tu peso en Mercurio es de:");
		double mercurio = 72 * .378;
		System.out.println(mercurio  + " kilos");
		
		System.out.print("Tu peso en Luna es de:");
		double luna = 72 * .166;
		System.out.println(luna + " kilos");
		
		System.out.print("Tu peso en Jupiter es de:");
		double jupiter = 72 * 2.364;
		System.out.println(jupiter + " kilos");
		
		

	}

}
